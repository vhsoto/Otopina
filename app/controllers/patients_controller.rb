  # POST /patients
  # POST /patients.json
  def create
    @patient = Patient.new(patient_params)

    respond_to do |format|
      if @patient.save
        format.html { redirect_to @patient, notice: 'Paciente ingresado satisfactoriamente.' }
        format.json { render :show, status: :created, location: @patient }
      else
        format.html { render :new }
        format.json { render json: @patient.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /patients/1
  # PATCH/PUT /patients/1.json
  def update
    respond_to do |format|
      if @patient.update(patient_params)
        format.html { redirect_to @patient, notice: 'Paciente actualizado satisfactoriamente.' }
        format.json { render :show, status: :ok, location: @patient }
      else
        format.html { render :edit }
        format.json { render json: @patient.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /patients/1
  # DELETE /patients/1.json
  def destroy
    @patient.destroy
    respond_to do |format|
      format.html { redirect_to patients_url, notice: 'Paciente eliminado satisfactoriamente.' }
      format.json { head :no_content }
    end
  end

  #GET /patients/search
  def search
    cedula = params[:cedula_paciente]
    @patient = Patient.find_by(document_number: cedula)
    respond_to do |format|
      if @patient.nil?
        format.html { render :notfound}
        format.js { render :notfound}
      else
        format.html { render :show }
        format.js
      end
    end
  end

